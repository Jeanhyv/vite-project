        <strong>Tipos primitivos</strong> y <strong>Objetos</strong>.
      </p>
    </section>

    <section class="ejemplos">
      <h2>🧩 Tipos de Datos Primitivos</h2>

      <div class="card">
        <h3>1️⃣ String (Cadenas de texto)</h3>
        <p>Representan texto. Se escriben entre comillas.</p>
        <pre><code>let nombre = "Jean";</code></pre>
        <button @click="mostrarTipo('Jean')">Ver tipo</button>
      </div>

      <div class="card">
        <h3>2️⃣ Number (Números)</h3>
        <p>Incluyen enteros y decimales.</p>
        <pre><code>let edad = 21;</code></pre>
        <button @click="mostrarTipo(21)">Ver tipo</button>
      </div>

      <div class="card">
        <h3>3️⃣ Boolean (Booleanos)</h3>
        <p>Solo tienen dos valores: <strong>true</strong> o <strong>false</strong>.</p>
