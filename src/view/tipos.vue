        <button @click="mostrarTipo(true)">Ver tipo</button>
      </div>

      <div class="card">
        <h3>4️⃣ Undefined</h3>
        <p>Indica que una variable ha sido declarada pero no tiene valor.</p>
        <pre><code>let x;</code></pre>
        <button @click="mostrarTipo(undefined)">Ver tipo</button>
      </div>

      <div class="card">
        <h3>5️⃣ Null</h3>
        <p>Representa la ausencia intencional de un valor.</p>
        <pre><code>let vacio = null;</code></pre>
        <button @click="mostrarTipo(null)">Ver tipo</button>
      </div>

      <div class="card">
        <h3>6️⃣ Symbol</h3>
        <p>Identificadores únicos y seguros, útiles para objetos.</p>
        <pre><code>let id = Symbol("id");</code></pre>
        <button @click="mostrarTipo(Symbol('id'))">Ver tipo</button>
      </div>

      <div class="card">
        <h3>7️⃣ BigInt</h3>
        <p>Permite trabajar con números enteros muy grandes.</p>
        <pre><code>let numeroGrande = 12345678901234567890n;</code></pre>
        <button @click="mostrarTipo(12345678901234567890n)">Ver tipo</button>
      </div>
    </section>

    <section class="ejemplos">
      <h2>🧱 Objetos y Valores</h2>

      <div class="card">
        <h3>📦 Objeto</h3>
        <p>Un objeto agrupa datos mediante pares <strong>clave: valor</strong>.</p>
        <button @click="mostrarObjeto">Mostrar objeto</button>
      </div>

      <div class="card">
        <h3>📚 Arreglo (Array)</h3>
        <p>Conjunto ordenado de valores.</p>
